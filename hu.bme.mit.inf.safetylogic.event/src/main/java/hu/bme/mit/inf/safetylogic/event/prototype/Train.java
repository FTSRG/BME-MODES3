package hu.bme.mit.inf.safetylogic.event.prototype;

public class Train {
	int id;
	double x, y, speed;
	String dir;

	public int getId() {
		return id;
	}

	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}

	public double getSpeed() {
		return speed;
	}

	public String getDir() {
		return dir;
	}
}
